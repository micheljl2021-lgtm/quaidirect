/**
 * Récupère la liste des ports pour un bassin donné
 */
export function getPortsByBasin(basin: FishingBasin): string[] {
  switch (basin) {
    case "MANCHE":
      return PORTS_MANCHE;
    case "ATLANTIQUE":
      return PORTS_ATLANTIQUE;
    case "MEDITERRANEE":
      return PORTS_MEDITERRANEE;
    default:
      return [];
  }
}

/**
 * Récupère tous les ports (toutes régions confondues)
 */
export function getAllPorts(): string[] {
  return [...PORTS_MANCHE, ...PORTS_ATLANTIQUE, ...PORTS_MEDITERRANEE];
}

